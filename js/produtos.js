function filtro_abela(){
  var divProduto = document.querySelectorAll('.produto');
  var qtdDivs = divProduto.length-1;
  for(let i=0; qtdDivs; i++){
    console.log(i, divProduto[i], divProduto[i].className)
    if(divProduto[i].className.indexOf('abela')!=-1){
      console.log('achou abela');
      divProduto[i].classList.remove('hide');
      console.log(divProduto[i].className);
      } else{
      console.log('não achou abela')
      divProduto[i].classList.add('hide');
      console.log(divProduto[i].className);
    }
  }
}

